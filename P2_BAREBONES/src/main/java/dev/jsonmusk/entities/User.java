package dev.jsonmusk.entities;

import java.util.Objects;

public class User {
    private int id;

    private String username, password, firstname, lastname, email;

    private byte[] photo;

    private boolean isLoggedIn;

    public User() {

    }
    public User(int id, String username, String password, boolean isLoggedIn) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.isLoggedIn = false;
    }

    public User(int id, String username, String password, boolean isLoggedIn, String firstname, String lastname, String email) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.firstname = firstname;
        this.lastname = lastname;
        this.email = email;
        this.isLoggedIn = isLoggedIn;
        this.photo = null;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", username='" + username + '\'' +
                ", password='" + password + '\'' +
                ", firstname='" + firstname + '\'' +
                ", lastname='" + lastname + '\'' +
                ", email='" + email + '\'' +
                ", photo=" + (photo != null? photo.length : "[]" ) +
                ", isLoggedIn=" + isLoggedIn +
                '}';
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public byte[] getPhoto() {
        return photo;
    }

    public void setPhoto(byte[] photo) {
        this.photo = photo;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        User user = (User) o;
        return id == user.id && password.equals(user.password) && username.equals(user.username);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, password, username);
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public boolean isLoggedIn() {
        return isLoggedIn;
    }

    public void setLoggedIn(boolean loggedIn) {
        isLoggedIn = loggedIn;
    }
}
